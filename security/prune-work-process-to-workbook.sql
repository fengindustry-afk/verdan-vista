       coalesce(
         case data->>'StageKey'
           when 'receiving'     then data->'Values'->>'pre_processing_date'
           when 'isolation'     then data->'Values'->>'pre_processing_date'
           when 'drying'        then data->'Values'->>'pre_processing_date'
